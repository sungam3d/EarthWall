def _fold(s: str) -> str:
    """Lowercase + strip diacritics so 'sao' matches 'São', 'cordoba'
    matches 'Córdoba', 'zurich' matches 'Zürich', etc. Uses NFKD
    decomposition and drops the combining marks."""
    import unicodedata
    return "".join(
        ch for ch in unicodedata.normalize("NFKD", s.lower())
        if not unicodedata.combining(ch)
    )


def city_label(city: tuple) -> str:
    """Human-readable 'City, Country' label for a database row. Used by the
    search UI so identically-named cities are distinguishable."""
    return f"{city[0]}, {city[1]}"


def search_cities(query: str, limit: int = 40) -> list[tuple]:
    """Case- and accent-insensitive search over city and country names.

    Ranking (best first):
      1. City name starts with the query
      2. City name contains the query
      3. Country name contains the query
    Within each tier the original (roughly importance-ordered) database
    order is preserved, so 'par' surfaces Paris before Paramaribo.
    """
    q = _fold(query.strip())
    if not q:
        return CITY_DATABASE[:limit]
    starts, contains, country = [], [], []
    for c in CITY_DATABASE:
        name_f = _fold(c[0])
        if name_f.startswith(q):
            starts.append(c)
        elif q in name_f:
            contains.append(c)
        elif q in _fold(c[1]):
            country.append(c)
    return (starts + contains + country)[:limit]


def find_city(label_or_name: str) -> tuple | None:
    """Resolve a 'City, Country' label (or a bare city name) back to its
    database row. Prefers an exact 'City, Country' match, then falls back
    to the first city with a matching (accent-folded) name."""
    s = label_or_name.strip()
    for c in CITY_DATABASE:
        if city_label(c) == s:
            return c
    low = _fold(s)
    for c in CITY_DATABASE:
        if _fold(c[0]) == low:
            return c
    return None
