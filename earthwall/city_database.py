def search_cities(query: str, limit: int = 20) -> list[tuple]:
    """Case-insensitive substring search over city and country names."""
    q = query.strip().lower()
    if not q:
        return CITY_DATABASE[:limit]
    matches = [c for c in CITY_DATABASE if q in c[0].lower() or q in c[1].lower()]
    return matches[:limit]
